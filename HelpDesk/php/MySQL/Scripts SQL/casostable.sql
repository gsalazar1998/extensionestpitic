CREATE TABLE casos (
	ID_CASO INT NOT NULL AUTO_INCREMENT,
	PROBLEMA VARCHAR(100) NOT NULL,
	DESCRIPCION TEXT(1000) NOT NULL,
	TIPO_PROBLEMA int,
	USUARIO VARCHAR(30) NOT NULL,
	NOMBRE VARCHAR(50) NOT NULL,
	EMAIL VARCHAR(30),
	NO_EMPLEADO INT,
	TELEFONO VARCHAR(20) NOT NULL,
	DEPTO VARCHAR(4) NOT NULL,
	OFICINA VARCHAR(3) NOT NULL,
	ASIGNADO_A VARCHAR(30),
	ARCHIVOS VARCHAR(255) ,
	ESTADO_CASO TINYINT NOT NULL,
	FECHA_APERTURA  DATETIME NOT NULL,
	FECHA_CIERRE DATETIME,
	CONFIRMACION BOOLEAN,
	PRIMARY KEY(ID_CASO)
);
